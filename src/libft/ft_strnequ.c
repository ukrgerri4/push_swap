#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 && s2)
	{
		while (n > 0 && *s1 && *s2)
		{
			if (*(s1++) != *(s2++))
				return (0);
			n--;
		}
		if (n != 0)
			if (*s1 != *s2)
				return (0);
		return (1);
	}
	return (0);
}
